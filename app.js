function makeTornPoints(){
  // Build a unique jagged silhouette around the image edge.
  // Points are percentages so CSS and canvas export can share the same shape.
  const pts = [];
  const steps = 5 + Math.floor(Math.random() * 4);
  const jitter = () => 1.5 + Math.random() * 8;
  for(let i=0;i<=steps;i++) pts.push([i/steps*100, jitter()]);
  for(let i=1;i<=steps;i++) pts.push([100-jitter(), i/steps*100]);
  for(let i=steps-1;i>=0;i--) pts.push([i/steps*100, 100-jitter()]);
  for(let i=steps-1;i>=1;i--) pts.push([jitter(), i/steps*100]);
  return pts.map(([x,y]) => `${x.toFixed(1)}% ${y.toFixed(1)}%`).join(', ');
}

function expandedPolygon(poly, amount = 2.4){
  return poly.replace(/([0-9.]+)% ([0-9.]+)%/g, (_, xs, ys) => {
    let x = Number(xs), y = Number(ys);
    x = x < 50 ? Math.max(0, x - amount) : Math.min(100, x + amount);
    y = y < 50 ? Math.max(0, y - amount) : Math.min(100, y + amount);
    return `${x.toFixed(1)}% ${y.toFixed(1)}%`;
  });
}

