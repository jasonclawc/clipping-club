  piece.dataset.h = String(Math.round(sizeRoll < 0.25 ? 110 + Math.random() * 100 : sizeRoll > 0.78 ? 240 + Math.random() * 190 : 150 + Math.random() * 175));
  // Clean crop mode: pieces are precise rectangular image crops, not torn scraps.
