function clearBoard(){
  boardPieces.forEach(p => p.remove());
  boardPieces = [];
  selected = null;
  tray.querySelectorAll('.clip').forEach(btn => {btn.classList.remove('used'); btn.disabled = false;});
  updateCount();
}

document.addEventListener('keydown', (e) => {
  if(!selected) return;
  if(e.key === 'Delete' || e.key === 'Backspace') changeSelected({del:true});
  if(e.key === '[') changeSelected({rot:-7});
  if(e.key === ']') changeSelected({rot:7});
  if(e.key === '=' || e.key === '+') changeSelected({scale:.08});
  if(e.key === '-') changeSelected({scale:-.08});
});

async function loadImage(src){
  return new Promise((resolve,reject) => { const img = new Image(); img.onload = () => resolve(img); img.onerror = reject; img.src = src; });
}
async function exportPNG(){
  const rect = artboard.getBoundingClientRect();
  const size = 1400;
  const canvas = document.createElement('canvas');
  canvas.width = size; canvas.height = size;
  const ctx = canvas.getContext('2d');
  const sx = size / rect.width;
  ctx.fillStyle = '#fff8e8'; ctx.fillRect(0,0,size,size);
  ctx.strokeStyle = 'rgba(18,16,13,.09)'; ctx.lineWidth = 1;
  for(let i=0;i<size;i+=28*sx){ ctx.beginPath(); ctx.moveTo(i,0); ctx.lineTo(i,size); ctx.stroke(); ctx.beginPath(); ctx.moveTo(0,i); ctx.lineTo(size,i); ctx.stroke(); }
  ctx.fillStyle = '#12100d'; ctx.fillRect(22,22,415,34); ctx.fillStyle = '#fff1d2'; ctx.font = '18px Menlo, monospace'; ctx.fillText(`CLIPPING CLUB / ${todayKey()}`, 34, 46);
  const orderedPieces = [...boardPieces].sort((a, b) => Number(a.style.zIndex || 0) - Number(b.style.zIndex || 0));
  for(const p of orderedPieces){ await drawImagePiece(ctx, p, sx); }
  const filename = `clipping-club-${todayKey()}.png`;
  canvas.toBlob(async (blob) => {
    if(!blob) return;
    const file = new File([blob], filename, { type: 'image/png' });
    const canShareFile = navigator.canShare && navigator.canShare({ files: [file] });
    if(canShareFile){
      try {
        await navigator.share({ files: [file], title: 'Clipping Club', text: 'My Clipping Club artifact' });
        return;
      } catch (err) {
        // If the user cancels native share, fall through to preview/save.
      }
    }
    const url = URL.createObjectURL(blob);
    const popup = window.open(url, '_blank');
    if(!popup){
      const a = document.createElement('a');
      a.href = url;
      a.download = filename;
      a.target = '_blank';
      document.body.appendChild(a);
      a.click();
      a.remove();
    }
    setTimeout(() => URL.revokeObjectURL(url), 60000);
  }, 'image/png');
}

function makeDatasetCutPath(ctx, w, h, poly){
  const fallback = '4% 0%, 100% 3%, 97% 95%, 0% 100%';
  const pairs = [...(poly || fallback).matchAll(/([0-9.]+)%\s+([0-9.]+)%/g)]
    .map(m => [Number(m[1]) / 100 * w, Number(m[2]) / 100 * h]);
  ctx.beginPath();
  pairs.forEach(([x,y], i) => i ? ctx.lineTo(x, y) : ctx.moveTo(x, y));
  ctx.closePath();
}


async function drawImagePiece(ctx, p, sx){
  const img = await loadImage(p.dataset.src);
  const x = parseFloat(p.dataset.x)*sx, y = parseFloat(p.dataset.y)*sx;
  const w = parseFloat(p.dataset.w || p.offsetWidth)*sx, h = parseFloat(p.dataset.h || p.offsetHeight)*sx;
  const scale = parseFloat(p.dataset.scale), rot = parseFloat(p.dataset.rot) * Math.PI / 180;
  ctx.save();
  ctx.translate(x + w/2, y + h/2); ctx.rotate(rot); ctx.scale(scale, scale); ctx.translate(-w/2, -h/2);
  ctx.shadowColor = 'rgba(18,16,13,.58)'; ctx.shadowOffsetX = 7; ctx.shadowOffsetY = 8; ctx.shadowBlur = 0;
  ctx.drawImage(img, 0, 0, w, h);
  ctx.restore();
}
