function todayKey(offset = 0){
  // Public daily challenge: use one shared Pulp day for everyone.
  // Packets rotate at midnight America/New_York instead of each visitor's local timezone.
  const now = new Date();
  const eastern = new Date(now.toLocaleString('en-US', { timeZone: 'America/New_York' }));
  eastern.setDate(eastern.getDate() + offset);
  return `${eastern.getFullYear()}-${String(eastern.getMonth()+1).padStart(2,'0')}-${String(eastern.getDate()).padStart(2,'0')}`;
}

function hashString(str){
  let h = 2166136261;
  for(let i=0;i<str.length;i++){ h ^= str.charCodeAt(i); h = Math.imul(h, 16777619); }
  return h >>> 0;
}

function seededShuffle(arr, seed){
  const out = [...arr];
  let s = seed >>> 0;
  const rand = () => { s = Math.imul(1664525, s) + 1013904223 >>> 0; return s / 4294967296; };
  for(let i=out.length-1;i>0;i--){ const j = Math.floor(rand() * (i+1)); [out[i],out[j]] = [out[j],out[i]]; }
  return out;
}

async function dailyPack(){
  const key = todayKey();
  promptEl.textContent = prompts[hashString(key) % prompts.length];
  try {
    const res = await fetch(`daily-packs/${key}.json?v=${encodeURIComponent(key)}`, { cache: 'no-store' });
    if(res.ok){
      const pack = await res.json();
      if(Array.isArray(pack.clippings) && pack.clippings.length >= LIMIT){
        return pack.clippings.slice(0, LIMIT);
      }
    }
  } catch (err) {
    console.warn('Daily pack unavailable, using local fallback', err);
  }
  // Emergency fallback only: the morning automation should create dated packs.
  return seededShuffle(clipLibrary, hashString(`clipping-club-${key}`)).slice(0, LIMIT);
}

async function renderTray(){
  tray.innerHTML = '<p class="tray-loading">Loading today’s clippings…</p>';
  todayPack = await dailyPack();
  tray.innerHTML = '';
  todayPack.forEach((clip, i) => {
    const btn = document.createElement('button');
    btn.type = 'button';
    btn.className = 'clip image-clip';
    btn.dataset.slug = clip.slug;
    btn.dataset.label = clip.label;
    btn.innerHTML = `<span class="clip-number">${String(i+1).padStart(2,'0')}</span><img src="${clip.src}" alt="${clip.label}" draggable="false"><span class="label">${clip.label}</span><span class="meta">${clip.meta}</span>`;
    btn.addEventListener('click', () => addPiece(clip, btn));
    tray.appendChild(btn);
  });
  updateCount();
}

